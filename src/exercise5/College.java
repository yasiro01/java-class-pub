package exercise5;

/**
 * Class College
 * Task 5: Create a class College with the following private data members: String name, int rank. Implement setter method for the rank.
 * @author Teboho Samuel Nteso
 */
public class College {
    String name;
    int rank;
    
    public College(String name, int rank){
        this.name = name;
        this.rank = rank;
    }
    
    public String getName(){
        return this.name = "Luther College";
    }
    public int setRank(){
        return this.rank = rank;
    }
    public int getRank() {
        return this.rank;
    }
    
}
